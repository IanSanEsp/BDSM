import express from "express";
import {
  crearGrupo,
  listarGrupos,
  actualizarGrupo,
  eliminarGrupo
} from "../controllers/gruposController.js";
import { requireAuth, requireAdmin } from "../middleware/auth.js";

const router = express.Router();

// Listar grupos
router.get("/", listarGrupos);

// joder c crudsitop
router.post("/", requireAuth, requireAdmin, crearGrupo);
router.put("/:id", requireAuth, requireAdmin, actualizarGrupo);
router.delete("/:id", requireAuth, requireAdmin, eliminarGrupo);

export default router;






























